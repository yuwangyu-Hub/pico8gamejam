function giftsmove(_g)
    if _g.mode=="down" then
        _g.y+=_g.spd
        if _g.y>=60 then
            _g.mode="up"
        end
    elseif _g.mode=="up" then
        _g.y-=_g.spd
        if _g.y<=20 then
            _g.mode="down"
        end
    elseif _g.mode=="fall" then
        _g.y+=3
    end
   
end

function inbounds(_s)
