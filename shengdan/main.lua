end

function shootdown(b,g)
    local qiqiu={
        x=g.x-1,
        y=g.y+3-g.long,
        w=6,
        h=6}
    local b={
        x=b.x+1,
        y=b.y,
        w=1,
        h=5}
    --æ£€æµ‹ç¢°æ’
    if not (qiqiu.x> b.x + b.w or
        qiqiu.x + qiqiu.w < b.x or
        qiqiu.y > b.y + b.h or
        qiqiu.y + qiqiu.h < b.y) then
        return true
    else
        return false  
    end
end



